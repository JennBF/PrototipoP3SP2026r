    public void llenadoDeTablas() {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("idPeliculas");
        modelo.addColumn("Nombre");
        modelo.addColumn("Clasificacion");
        modelo.addColumn("Genero");
        modelo.addColumn("Idioma");
        modelo.addColumn("Subtitulado");
        modelo.addColumn("precio");

        CinesDAO cinesDAO = new CinesDAO();
        List<Cines> cines = cinesDAO.select(); // Obtiene la lista de películas

        tablaPeliculas.setModel(modelo);
        String[] dato = new String[7];

        for (int i = 0; i < cines.size(); i++) {
            dato[0] = Integer.toString(cines.get(i).getIdPeliculas());
            dato[1] = cines.get(i).getNombre();
            dato[2] = cines.get(i).getClasificacion();
            dato[3] = cines.get(i).getGenero();
            dato[4] = cines.get(i).getIdioma();
            dato[5] = cines.get(i).getSubtitulado();
            dato[6] = Double.toString(cines.get(i).getPrecio());
            modelo.addRow(dato);
        }
    }
    public void buscarPelicula() {
        CinesDAO cinesDAO = new CinesDAO();
        Cines cineAConsultar = new Cines();
        cineAConsultar.setIdPeliculas(Integer.parseInt(txtbuscado.getText()));
        cineAConsultar = cinesDAO.query(cineAConsultar);

        if (cineAConsultar != null) {
            idPeliculastxt.setText(Integer.toString(cineAConsultar.getIdPeliculas()));
            Nombretxt.setText(cineAConsultar.getNombre());
            Clasificaciontxt.setText(cineAConsultar.getClasificacion());
            Generotxt.setText(cineAConsultar.getGenero());
            Idiomatxt.setText(cineAConsultar.getIdioma());
            Subtituladotxt.setText(cineAConsultar.getSubtitulado());
            preciotxt.setText(Double.toString(cineAConsultar.getPrecio()));
        } else {
            JOptionPane.showMessageDialog(null, "Película no encontrada.",
                "Información del Sistema", JOptionPane.WARNING_MESSAGE);
        }
    }
