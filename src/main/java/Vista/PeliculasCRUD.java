        cineAInsertar.setNombre(Nombretxt.getText());
        cineAInsertar.setClasificacion(Clasificaciontxt.getText());
        cineAInsertar.setGenero(Generotxt.getText());
        cineAInsertar.setIdioma(Idiomatxt.getText());
